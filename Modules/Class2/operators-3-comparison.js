/**
 * comparison Operator (>=, <=)
 */

let num1 = 10;
let num2 = 20;

let num11GrEqNum2 = num1 >= num2;
console.log(`${num1} >= ${num2} = ${num11GrEqNum2}`);

/**
 * === to compare if two values are equal in value and same datatype
 * == to compare if two values are equal in value
 */

let num11EqNum2 = num1 === num2;
console.log(`\n${num1} === ${num2} = ${num11EqNum2}`);



/**
 * !== to compare if two values are NOT equal in value and same datatype
 * != to compare if two values are NOT equal in value
 */
let num3 = 30;
let num4 = 40;

let num2NotEqNum4 = num3 !== num4;
console.log(`\n${num3} !== ${num4} = ${num2NotEqNum4}`);

